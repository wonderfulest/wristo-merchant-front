const messages: Record<SupportedLocale, Messages> = {
  de: en,
  en,
  es: en,
  fr: en,
  it: en,
  zh,
}

export function translate(key: MessageKey, locale: SupportedLocale, vars?: Record<string, string | number>): string {
  const template = messages[locale]?.[key] || en[key] || key
  if (!vars) return template
  return Object.entries(vars).reduce(
    (text, [name, value]) => text.split(`{${name}}`).join(String(value)),
    template
  )
}

export function useI18n() {
  const localeStore = useLocaleStore()
  const locale = computed(() => localeStore.currentLocale)
  const t = (key: MessageKey, vars?: Record<string, string | number>) => translate(key, locale.value, vars)
  return { locale, t }
}
